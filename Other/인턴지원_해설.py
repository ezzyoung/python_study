import ast

def BE2ndDiagonal(strArr):
    if isinstance(strArr, str):
        varFiltersCg = ast.literal_eval(strArr)
    else:
        varFiltersCg = strArr

    matrix = [ast.literal_eval(row) if isinstance(row, str) else row
              for row in varFiltersCg]

    varOcg = []
    R = len(matrix)
    C = len(matrix[0]) if R > 0 else 0

    for sc in range(C):
        r, c = 0, sc
        while r < R and c >= 0:
            varOcg.append(matrix[r][c])
            r += 1
            c -= 1

    for sr in range(1, R):
        r, c = sr, C - 1
        while r < R and c >= 0:
            varOcg.append(matrix[r][c])
            r += 1
            c -= 1

    return ", ".join(map(str, varOcg))

