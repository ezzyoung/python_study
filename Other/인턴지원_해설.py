import requests
import json

def clean(v, filters):
  if isinstance(v, dict):
    return {k: clean(val, filters) for k, val in v.items()
    if not (isinstance(val, str) and val in filters)}

  if isinstance(v, list):
    return [clean(i, filters) for i in v
    if not (isinstance(i, str) and i in filters)]

  return v

